DeletePoints:
  loadn R0, #0
  store score, R0
  jmp startGame


ApagaTela:
  push R0
  push R1
  push R2

  loadn R0, #0      ; Posio inicial na tela
  loadn R1, #32     ; Cdigo ASCII de espao (' ')
  loadn R2, #1200   ; Nmero total de caracteres a apagar
  ApagaTela_Loop:
    outchar R1, R0    ; Imprime um espao na posio atual
    inc R0            ; Avana para o prximo caractere
    dec R2            ; Decrementa contador de caracteres a limpar
    jnz ApagaTela_Loop ; Continua at R2 ser 0
  pop R2
  pop R1
  pop R0
  rts

