;; ----------------------------------Funções para o Score --------------------

AtualizaScore:
  push R0
  push R1
  push R2
  push R3
  push R4
  push R5

  load R0, posPassaro     ; Pega a posição do pássaro
  load R1, cimaPosition   ; Pega a posição do obstáculo superior
  load R2, baixoPosition  ; Pega a posição do obstáculo inferior

  loadn R3, #40           ; Armazena 40 em um registrador para o mod
  mod R0, R0, R3          ; Calcula posPassaro % 40
  mod R1, R1, R3          ; Calcula cimaPosition % 40
  mod R2, R2, R3          ; Calcula baixoPosition % 40

  cmp R0, R1              ; Verifica se o pássaro está na mesma coluna do obstáculo superior
  jeq SomaScore

  cmp R0, R2              ; Verifica se o pássaro está na mesma coluna do obstáculo inferior
  jeq SomaScore

  jmp FimScore

SomaScore:
  load R4, score
  inc R4                  ; Incrementa score
  store score, R4
  call ExibirScore        ; Atualiza o score na tela

FimScore:
  pop R5
  pop R4
  pop R3
  pop R2
  pop R1
  pop R0
  rts


ExibirScore:
  push R0
  push R1
  push R2
  push R3
  push R4
  push R5

  load R0, score          ; Carrega o score
  loadn R1, #1100         ; Define a posição inicial do score na tela

  loadn R3, #10           ; Armazena 10 para a operação de divisão
  loadn R4, #48           ; Código ASCII do '0'

ExibirScoreLoop:
  mod R2, R0, R3          ; Obtém o último dígito do score
  add R2, R2, R4          ; Converte para código ASCII ('0' + dígito)
  outchar R2, R1          ; Exibe o dígito na posição da tela

  div R0, R0, R3          ; Remove o último dígito do score
  dec R1                  ; Move para a esquerda na tela para o próximo dígito

  loadn R5, #0            ; Carrega 0 para comparação
  cmp R0, R5              ; Se o número for 0, para
  jne ExibirScoreLoop

  pop R5
  pop R4
  pop R3
  pop R2
  pop R1
  pop R0
  rts


