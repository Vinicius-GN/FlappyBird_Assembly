DetectaColisao:
  push R0   ; posPassaro
  push R1   ; cimaPosition
  push R2   ; baixoPosition
  push R3   ; TamCima
  push R4   ; TamBaixo
  push R5   ; posio vertical do pssaro
  push R6   ; posio vertical do obstculo
  push R7   ; posio horizontal do pssaro

  ;; ------------------------ Carregar valores essenciais ------------------------

  load R0, posPassaro    ; Posio do pssaro
  load R1, cimaPosition  ; Posio do obstculo superior
  load R2, baixoPosition ; Posio do obstculo inferior
  load R3, TamCima       ; Tamanho do obstculo superior
  load R4, TamBaixo      ; Tamanho do obstculo inferior

  loadn R6, #40          ; Valor fixo para diviso e mdulo

  ;; ------------------------ Clculo da posio vertical do pssaro ------------------------

  div R5, R0, R6         ; R5 = posPassaro // 40
  inc R5                 ; R5 = (posPassaro // 40) + 1

  ;; ------------------------ Verificar coliso com o cho------------------------------------

  loadn R7, #28
  cmp R5, R7
  jeq rotinaDerrota

  ;; ------------------------ Verificar coliso com o obstculo superior ------------------------

  ;; Clculo da altura do obstculo superior: (2 + tamCima)
  loadn R7, #2
  add R7, R7, R3         ; R7 = 2 + TamCima

  ;; Se (posPassaro // 40) + 1 > (2 + tamCima), pula para VerificaBaixo
  cmp R5, R7
  jle VerificaCimaHorizontal  ; Se R5 <= R7, pode haver coliso

  jmp VerificaBaixo      ; Se no, verifica o obstculo inferior

VerificaCimaHorizontal:
  ;; Verificao horizontal: Se (posPassaro mod 40) == (posCima mod 40)
  mod R7, R0, R6         ; R7 = posPassaro mod 40
  mod R3, R1, R6         ; R3 = posCima mod 40
  cmp R7, R3
  jne VerificaBaixo      ; Se no estiverem na mesma coluna, continua

  jmp rotinaDerrota      ; Coliso detectada, fim do jogo!

  ;; ------------------------ Verificar coliso com o obstculo inferior ------------------------

VerificaBaixo:
  ;; Clculo da altura do obstculo inferior: (posBaixo // 40) + 1
  div R7, R2, R6         ; R7 = posBaixo // 40

  ;; Se (posPassaro // 40) + 1 < (posBaixo // 40) + 1, no h coliso
  cmp R7, R5
  jle VerificaBaixoHorizontal  ; Se R5 >= R7, pode haver coliso

  jmp FimColisao        ; Se no, no h coliso e retorna

VerificaBaixoHorizontal:
  ;; Verificao horizontal: Se (posPassaro mod 40) == (posBaixo mod 40)
  mod R7, R0, R6         ; R7 = posPassaro mod 40
  mod R3, R2, R6         ; R3 = posBaixo mod 40
  cmp R7, R3
  jne FimColisao         ; Se no estiverem na mesma coluna, continua

  jmp rotinaDerrota      ; Coliso detectada, fim do jogo!

  ;; ------------------------ Fim da verificao ------------------------

FimColisao:
  pop R7
  pop R6
  pop R5
  pop R4
  pop R3
  pop R2
  pop R1
  pop R0
  rts



rotinaDerrota:
  push R0
  call ApagaTela
  loadn R0, #derrota
  call prinDerrota

  call ExibirScore      ; Mostra o score final

  pop R0

  call keyboard         ; Aguarda entrada do jogador
  loadn R1, #'1'        ; Se pressionar '1', reinicia o jogo
  cmp R3, R1
  jeq DeletePoints

  loadn R1, #'2'        ; Se pressionar '2', volta ao menu
  cmp R3, R1
  jeq main

  jmp rotinaDerrota     ; Se no pressionou nada vlido, espera de novo


