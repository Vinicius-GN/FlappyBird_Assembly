DetectaColisao:
  push R0   ; posPassaro
  push R1   ; cimaPosition
  push R2   ; baixoPosition
  push R3   ; TamCima
  push R4   ; TamBaixo
  push R5   ; posição vertical do pássaro
  push R6   ; posição vertical do obstáculo
  push R7   ; posição horizontal do pássaro

  ;; ------------------------ Carregar valores essenciais ------------------------

  load R0, posPassaro    ; Posição do pássaro
  load R1, cimaPosition  ; Posição do obstáculo superior
  load R2, baixoPosition ; Posição do obstáculo inferior
  load R3, TamCima       ; Tamanho do obstáculo superior
  load R4, TamBaixo      ; Tamanho do obstáculo inferior

  loadn R6, #40          ; Valor fixo para divisão e módulo

  ;; ------------------------ Cálculo da posição vertical do pássaro ------------------------

  div R5, R0, R6         ; R5 = posPassaro // 40
  inc R5                 ; R5 = (posPassaro // 40) + 1

  ;; ------------------------ Verificar colisão com o chão------------------------------------

  loadn R7, #28
  cmp R5, R7
  jeq rotinaDerrota

  ;; ------------------------ Verificar colisão com o obstáculo superior ------------------------

  ;; Cálculo da altura do obstáculo superior: (2 + tamCima)
  loadn R7, #2
  add R7, R7, R3         ; R7 = 2 + TamCima

  ;; Se (posPassaro // 40) + 1 > (2 + tamCima), pula para VerificaBaixo
  cmp R5, R7
  jle VerificaCimaHorizontal  ; Se R5 <= R7, pode haver colisão

  jmp VerificaBaixo      ; Se não, verifica o obstáculo inferior

VerificaCimaHorizontal:
  ;; Verificação horizontal: Se (posPassaro mod 40) == (posCima mod 40)
  mod R7, R0, R6         ; R7 = posPassaro mod 40
  mod R3, R1, R6         ; R3 = posCima mod 40
  cmp R7, R3
  jne VerificaBaixo      ; Se não estiverem na mesma coluna, continua

  jmp rotinaDerrota      ; Colisão detectada, fim do jogo!

  ;; ------------------------ Verificar colisão com o obstáculo inferior ------------------------

VerificaBaixo:
  ;; Cálculo da altura do obstáculo inferior: (posBaixo // 40) + 1
  div R7, R2, R6         ; R7 = posBaixo // 40

  ;; Se (posPassaro // 40) + 1 < (posBaixo // 40) + 1, não há colisão
  cmp R7, R5
  jle VerificaBaixoHorizontal  ; Se R5 >= R7, pode haver colisão

  jmp FimColisao        ; Se não, não há colisão e retorna

VerificaBaixoHorizontal:
  ;; Verificação horizontal: Se (posPassaro mod 40) == (posBaixo mod 40)
  mod R7, R0, R6         ; R7 = posPassaro mod 40
  mod R3, R2, R6         ; R3 = posBaixo mod 40
  cmp R7, R3
  jne FimColisao         ; Se não estiverem na mesma coluna, continua

  jmp rotinaDerrota      ; Colisão detectada, fim do jogo!

  ;; ------------------------ Fim da verificação ------------------------

FimColisao:
  pop R7
  pop R6
  pop R5
  pop R4
  pop R3
  pop R2
  pop R1
  pop R0
  rts



rotinaDerrota:
  push R0
  call ApagaTela
  loadn R0, #derrota
  call prinDerrota

  call ExibirScore      ; Mostra o score final

  pop R0

  call keyboard         ; Aguarda entrada do jogador
  loadn R1, #'1'        ; Se pressionar '1', reinicia o jogo
  cmp R3, R1
  jeq DeletePoints

  loadn R1, #'2'        ; Se pressionar '2', volta ao menu
  cmp R3, R1
  jeq main

  jmp rotinaDerrota     ; Se não pressionou nada válido, espera de novo


