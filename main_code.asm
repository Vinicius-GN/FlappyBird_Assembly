ApagaTela:
  push R0
  push R1
  push R2

  loadn R0, #0      ; Posição inicial na tela
  loadn R1, #32     ; Código ASCII de espaço (' ')
  loadn R2, #1200   ; Número total de caracteres a apagar
  ApagaTela_Loop:
    outchar R1, R0    ; Imprime um espaço na posição atual
    inc R0            ; Avança para o próximo caractere
    dec R2            ; Decrementa contador de caracteres a limpar
    jnz ApagaTela_Loop ; Continua até R2 ser 0
  pop R2
  pop R1
  pop R0
  rts

